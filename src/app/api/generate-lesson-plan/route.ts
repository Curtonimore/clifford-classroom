// Function to generate a demo lesson plan based on the subject
function generateDemoLessonPlan(subject, audience, topic, time, standards, objectives, options) {
  // Choose the most appropriate template based on subject
  let lessonPlan = '';
  
  // Map the subject names from the form to the template keys
  const subjectMap = {
    'Mathematics': 'mathematics',
    'Science': 'science',
    'Language Arts': 'language_arts',
    'Social Studies': 'social_studies'
  };
  
  // Get the subject key for the template
  const subjectKey = subjectMap[subject] || Object.keys(SAMPLE_LESSON_PLANS)[0];
  
  // Get the template
  lessonPlan = SAMPLE_LESSON_PLANS[subjectKey] || SAMPLE_LESSON_PLANS.mathematics;
  
  // Customize the lesson plan title and audience
  let customTitle = `# ${topic} Lesson Plan`;
  if (audience) {
    customTitle += ` for ${audience}`;
  }
  lessonPlan = lessonPlan.replace(/^# .*$/m, customTitle);
  
  // Add the selected options to the learning objectives if provided
  if (options && options.length > 0) {
    // Find the Learning Objectives section
    const objectivesPattern = /## Learning Objectives\n([^#]*)/;
    const objectivesMatch = lessonPlan.match(objectivesPattern);
    
    if (objectivesMatch) {
      let objectivesSection = objectivesMatch[1];
      // Add the selected options as new objectives
      objectivesSection += `- Focus on specific aspects: ${options}\n`;
      
      // Replace the original objectives section
      lessonPlan = lessonPlan.replace(objectivesPattern, `## Learning Objectives\n${objectivesSection}`);
    }
  }
  
  // Add custom duration if provided
  if (time) {
    lessonPlan = lessonPlan.replace(
      /### Direct Instruction \((\d+) minutes\)/,
      `### Direct Instruction (adjusted for ${time})`
    );
  }
  
  // Add custom standards if provided
  if (standards) {
    const standardsPattern = /## Standards Addressed\n([^#]*)/;
    const standardsMatch = lessonPlan.match(standardsPattern);
    
    if (standardsMatch) {
      let standardsSection = `- ${standards}\n`;
      
      // Replace the original standards section
      lessonPlan = lessonPlan.replace(standardsPattern, `## Standards Addressed\n${standardsSection}`);
    }
  }
  
  // Add custom objectives if provided
  if (objectives) {
    const objectivesPattern = /## Learning Objectives\n([^#]*)/;
    const objectivesMatch = lessonPlan.match(objectivesPattern);
    
    if (objectivesMatch) {
      let objectivesSection = `- ${objectives}\n`;
      
      // Replace the original objectives section
      lessonPlan = lessonPlan.replace(objectivesPattern, `## Learning Objectives\n${objectivesSection}`);
    }
  }
  
  // Add a note at the top about this being a demo
  lessonPlan = `> Note: This is a sample lesson plan template customized with your input. For fully personalized lesson plans, a valid AI API key would be required.\n\n${lessonPlan}`;
  
  return lessonPlan;
}

export async function POST(request: NextRequest) {
  try {
    // Get form data from request
    const { standards, audience, time, subject, topic, objectives, options } = await request.json();
    
    // Generate demo lesson plan (no API key needed)
    const lessonPlan = generateDemoLessonPlan(subject, audience, topic, time, standards, objectives, options);
    
    // Add a small delay to simulate processing
    await new Promise(resolve => setTimeout(resolve, 1500));
    
    return NextResponse.json({ 
      lessonPlan,
      metadata: {
        standards,
        audience,
        time,
        subject,
        topic,
        objectives,
        options,
        generatedAt: new Date().toISOString()
      }
    });
  } catch (error: any) {
    console.error("Error in lesson plan generation:", error);
    return NextResponse.json(
      { 
        error: "Failed to generate lesson plan",
        message: error.message || "An unexpected error occurred."
      },
      { status: 500 }
    );
  }
} 